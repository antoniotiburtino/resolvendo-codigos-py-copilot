# 5 - Calculando Media de Notas
# Agora vamos calcular a média de três notas fornecidas na entrada do usuário. Uma dica é: Utilize operadorees aritméticos para realizar o cálculo da média.

nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))

media = (nota1 + nota2) / 2

print(f"A média da nota é: {media} ")